                ./(2.*(3./(4.*pi.*xx)).^(1/6)+b))-b.*x0./(x0^2+b*x0+c).*(log(((3./(4*pi.*xx))...
                .^(1/6)-x0).^2./(((3./(4*pi.*xx)).^(1/3))+b.*((3./(4*pi.*xx)).^(1/6))+c))+2*(b+2*x0)...
                ./sqrt(4*c-b.^2).*atan(sqrt(4*c-b^2)./(2.*(3./(4.*pi.*xx)).^(1/6)+b))));
        end
    end
end

    